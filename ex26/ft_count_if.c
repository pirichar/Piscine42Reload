int	ft_count_if(char **tab, int (*f)(char*))
{
	int	rtn;
	int	i;

	rtn = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
		{
			rtn++;
		}
		i++;
	}
	return (rtn);
}
